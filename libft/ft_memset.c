#include"libft.h"

void	*ft_memset(void *str, int x, size_t i)
{
	unsigned char	*k;
	int				b;

	b = 0;
	k = str;
	while (i != 0)
	{
		k[b] = (char)x ;
		i--;
		b++;
	}
	return (str);
}
